package com.arraylist_test21;

public class Employee1 {
	private int id;
	private String name;
	public Employee1(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	

}
